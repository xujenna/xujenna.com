<p><i>Mad Libs​</i> is a voice-controlled browser “game” that guides two people towards the resolution of a conflict of their choosing. Using a mad libs format and the application of research on the psychology of communication and conflict resolution, the flow of the game structures the interpersonal issue as a problem that requires cooperation to solve, and optimizes for empathy as well as clear, effective, and (relatively) emotionally neutral communication. The game orally guides participants through seven steps and transcribes what users say during each turn, highlighting both problematic and cooperative speech for future reference. It penalizes the use of absolutist phrases, rewards spoken bonus scripts that express empathy, and counts the use of different pronouns. The main goal is to resolve the issue at hand, but also to produce an artifact that can be discussed and learned from after the challenge is completed.
</p>
<p><i>Mad Libs​</i> utilizes HTML5’s Speech Synthesis API to orally guide participants through a flow, as well as HTML5’s Speech Recognition API to transcribe, penalize, reward, and count the occurence of pronouns. Eventually, a sentiment analysis model will be trained on Twitter arguments (manually labeled by researchers for level of nastiness), and implemented in tensorflow.js in order to penalize uncooperativeness and anger.</p>
