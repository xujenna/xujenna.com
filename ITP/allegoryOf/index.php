<p><i>Self-Portrait as Allegory of</i> is my masters's thesis project: a data-driven, machine-managed, sanity maintenance system.</p>
<p>
Adapting the conceptual format and title of an Artemisia Gentileschi painting from 1638–39, it is at once a self-portrait as depicted by personal data and predictive models, as well as an allegory to an amalgamation of scientific, cultural, and personal ideals of mental health.
</p>
<p>
In construction, <i>Self-Portrait as Allegory of</i> comprises three systems: data collection by a body of trackers I built to collect digital phenotypes; mood prediction by a suite of neural networks that use this data to predict my mood, morale, stress, and fatigue levels; and intervention by a noninteractive voice assistant that responds to these predictions by directing me to perform mood-improving interventions and rituals that, taken together, form a lifestyle shift that optimizes for mental health.
<br>
</p>
