<p>In H1 of 2020, I led a redesign and restructuring of Mindstrong's mobile app; this is a visual history of my explorations.</p>
<p>
Because our population was older, of lower socioeconomic status and education levels, and living with SMI, my guiding principles for the visual redesign were simplicity and accessibility, accommodating both physical and mental disabilities:
	<ul>
	<li>Copy was to be concise, straightforward, and conversational.</li>
	<li>Type was to be large and the center of attention.</li>
	<li>UX was to be guided and on-the-rails, with every task broken down into smaller steps. </li>
	<li>UI was to be kept to a minimum.</li>
	<li>Visuals were to be friendly and comforting.</li>

</ul>
</p>
<p>Illustrations featured in the following screens are by <a href="http://www.abbeylossing.com/" target="new">Abbey Lossing</a>, <a href="http://www.chrisdelorenzo.com/" target="new">Christopher Lorenzo</a>, and <a href="http://amymartino.com/" target="new">Amy Martino</a>.</p>
