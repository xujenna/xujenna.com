<p>
Following a pivot to youth sports, Optimism Health’s founding team brought me on for three months as their first design hire to overhaul the app for hockey players aged 13-18. The original experience was built for data-driven health optimizers and offered little appeal for teenage users. Opti, their AI assistant coach, especially struggled to engage them in conversation. With only two engineers and myself as the sole designer, the challenge was to deliver a high-impact redesign before hockey season began in three months.
</p>
<p>
<b>Goals:</b> Revamp the app to boost engagement with youth athletes in order to demonstrate value to their coaches—the customer—and build on the momentum of their initial traction in this new market. To make the most of limited time and resources during my 3-month engagement, we prioritized strategic UX enhancements and lightweight visual updates, with a full rebrand planned for a later phase.
</p>
<h2 class="section_hed">Process</h2>
<div class="type_test" id="description_text">
<p class="subhead">
<b>User Research</b>
</p><p>
	I conducted interviews to understand youth athletes’ motivations and pain points, revealing several key insights:
<ul>
	<li>
		All players cited being motivated by seeing improvement in their own personal fitness, expressing desire to <i>“be their best selves”</i>.
	</li>
	<li>
		All players cited their team as major motivators in keeping up with on- and off-ice fitness regimens, and valued the <i>sense of community and connection</i> their team provided. Interestingly, they all expressed interest in <i>friendly competition</i> with their teammates as well.
	</li>
	<li>
		Despite insisting that Opti was helpful, many players skipped conversations with the AI coach, blaming repetitive, bland topics as well as their own <i>perceived impatience and disinterest</i>.
	</li>
	<li>
		Despite the survey-like format, <i>players treated check-ins more like checklists</i>, often returning to the app throughout the day to log progress. This misalignment highlighted the need for a new interface that matched user behavior.
	</li>
</ul>
</p>
<p class="subhead">
<b>Ideation & Refinement</b>
</p><p>Because interviews were scheduled on a rolling basis, I started by designing low-hanging usability improvements in high fidelity to seed discussions with our founding team. Eventual UXR findings enabled me to prioritize and develop new and improved features, which I expressed with high-fidelity mockups in Figma to ensure productive conversations with the founding team (who, by their own admission, knew nothing about design!). This also allowed us to better negotiate resource constraints and plan for a phased rollout with maximum impact.
</p>
</div>
<h2 class="section_hed">Final Designs</h2>
<div class="type_test" id="description_text">
<p class="subhead">
<b>Usability improvements</b>
</p><p>The existing web app, built by the founders, had limited functionality and posed usability challenges, such as small fonts, non-standard form inputs, and a pop-up chat window for Opti that made interactions cumbersome.<br>
<img class="img-responsive" src="./usability.png"><br>
</p>
<p class="subhead">
<b>A homescreen that mirrors your best self</b>
</p><p>
What’s more motivating than a good old visualization? When users now login, they immediately see their personal records as a reminder of their own potential—as well as an incentive to complete their check-in! I also introduced more forgiving check-in streaks, which allow users to claw back their streaks by retroactively completing check-ins.<br>
<img class="img-responsive" src="./homescreen.png"><br>
</p>
<p class="subhead">
<b>Leveraging the team to improve the player</b>
</p><p>I introduced team-branded skins as an easy way to tap into a major user motivator (community) while punting on visual direction (knowing a branding exercise was coming). Additionally, subtle leaderboards, as well as the presence of teammate profiles, added easy elements of healthy competition.<br>
<img class="img-responsive" src="./team_skins.png"><br>
</p>
</div>
<h2 class="section_hed">Future Designs</h2>
<div class="type_test" id="description_text">
<p class="subhead">
<b>A new task management paradigm</b>
</p><p>
Armed with user data and collaborative impact mapping with the founders, I successfully proposed and designed a significant workflow redesign for their future native app. The redesign transformed the overburdened, survey-style check-in into a task management system that not only better supports the hidden behavior-changing functionality, but also allows players to track and log activities throughout the day. This paradigm shift aligned more naturally with how players were engaging with check-ins, as well as with the existing check-in survey logic that dynamically adapted to activity cadences and user behavior. By improving alignment across design, functionality, and user behavior and expectation, the new paradigm can seamlessly foster adherence and habit formation.<br>
<img class="img-responsive" src="./task_paradigm.png"><br>
</p>
<p class="subhead">
<b>A new Opti</b>
</p><p>Because Opti only and always appeared after completing a check-in, it was difficult to come back after the user decided the conversations weren't worth having. And it was clear from the data that users didn't think talking to Opti was important. I developed a strategy to free Opti from check-ins: the new "Async Opti" would start conversations for meaningful reasons, ie trending task completions or incompletions, and tailored conversation topics and openers based on players’ behaviors, values, and interests. Separating Opti from check-in completions, and triggering it instead based on user behavior, actually affords more opportunities for more relevant and meaningful conversations, while lowering the volume overall.<br>
<img class="img-responsive" src="./async_opti.png"><br>
</p>
<p class="subhead">
<b>Future ways to interact with the team</b>
</p><p>
Additional features for their upcoming native app include messaging threads, event coordination features, and calendars, which would enable Optimism Health to replace more rudimentary competitors currently being used.<br>
<img class="img-responsive" src="./native_app.png"><br>
</p>
</div>
<h2 class="section_hed">Implementation, Outcome, and Next Steps</h2>
<div class="type_test" id="description_text">
	<p>
We rolled out the top three items in time for the start of hockey season, while doing ongoing testing with Opti’s conversation starters. The native app and concomitant task management system, async Opti, and additional team features are scheduled for implementation in Q4. </p>
<p>New designs have significantly increased traction with the business’s new target customer, youth hockey coaches. The redesigned app was used in demos with interested coaches and drove the acquisition of 10+ new teams and counting. End users reported significantly favoring the redesign as well.
</p>
<p>
Next steps include monitoring engagement with the app, especially with check-ins and Opti conversations, as well as a rebrand currently underway.
</p>
</div>
